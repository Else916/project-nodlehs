							<h3 class="title"><span>Policy and Procedures</span></h3>
							
							<h4>Special Education Needs – Policy</h4>

							<h4>Purpose</h4>

							<p>The purpose of Special Education Needs (SEN) is to ensure that all students with special needs are eligible to receive special education programs and services. A meeting is to be held for this purpose and a plan is to be developed. This plan, according to the identified area may result in an:</p>

							<ul>
								<li><b>IEP</b> (Individualized Learning Plan)</li>
								<li><b>ALP</b> (Advanced Learning Plan)</li>
								<li><b>PLP</b> (Personalized Learning Plan)</li>
								<li><b>BMP</b> (Behavioral Management Plan)</li>
							</ul>

							<h4>Policy for IEP</h4>

							<ol>
								<li><b>IEP Overview {* Refer to Addendum D}</b>
									<p>An IEP is a written statement for a child with a disability that is developed, reviewed, and revised in a team meeting. During the meeting, the team specifies the individual educational needs of the child and what special education and related services are necessary to meet the child’s educational needs.</p>
								</li>
								<li><b>Eligibility</b>
									<p>Students who are eligible for IEPs must be diagnosed with one or more of the following disabilities:</p>
									<ul>
										<li>Autism</li>
										<li>Deafness</li>
										<li>Blindness</li>
										<li>Developmental Delay</li>
										<li>Emotional Disability</li>
										<li>Hearing Impairment</li>
										<li>Intellectual Disability</li>
										<li>Multiple Disabilities</li>
										<li>Orthopedic Impairment</li>
										<li>Specific Learning Disability</li>
										<li>Speech-Language Impairment</li>
										<li>Traumatic Brain Injury</li>
										<li>Visual Impairment </li>
									</ul>
								</li>
								<li><b>Persons responsible for the development of the IEP</b>
									<ul>
										<li>At least one regular education teacher</li>
										<li>At least one special education teacher</li>
										<li>Subject coordinator</li>
										<li>SEN coordinator</li>
										<li>Supervisor</li>
										<li>School /external psychologist</li>
										<li>School counselor</li>
										<li>School administration</li>
										<li>Parents</li>
										<li>Students (if appropriate for G6 and above)</li>
									</ul>
								</li>
								<li><b>Referral</b>
									<ul>
										<li>Written referrals to the SENCO may be made by any school personnel and will be received by the school principal or any other person as designated by the principal.</li>
										<li>SEN team members receive notification of a proposed SEN meeting and SEN team meets and reviews referrals received.</li>
										<li>SEN team conducts a review of students’ academic and performance history, medical history and conducts classroom observations in the area of concern.</li>
										<li>SEN team makes recommendations for interventions intended to improve the students’ academic performance and develop an IEP accordingly.</li>
										<li>SEN team monitors the students’ progress and reviews data weekly for discussion.</li>
										<li>SEN team communicates closely with parents on a monthly basis, and lets them know if goals/objectives have been met.</li>
									</ul>
								</li>
							</ol>

							<h4>Policy for ALP</h4>

							<ol>
								<li><b>ALP Overview</b>
									<p>The purpose of the plan is to identify gifted/talented students. They will have an Advanced Learning Plan (ALP) created for them by their school teachers, coordinator SEN team and administration where appropriate. A gifted child is a child who has the ability to perform in any domain of human ability, and achieve results, at a level we would usually expect of students some years older.</p>
								</li>
								<li><b>Eligibility</b>
									<p>The process involves evaluating a child’s raw intellectual ability, comparing the child’s performance on a Cognitive Abilities Test(CAT) to the performance of other children of the same age and sex, on the same tasks. A performance that is notably above average indicates exceptional cognitive abilities.</p>
								</li>
								<li><b>Persons responsible for the development of the ALP</b>
									<ul>
										<li>At least one regular education teacher</li>
										<li>At least one special education teacher </li>
										<li>Subject coordinator</li>
										<li>SEN coordinator</li>
										<li>School administration</li>
										<li>Parents</li>
										<li>Students (if appropriate for G6 and above)</li>
									</ul>
								</li>
								<li><b>Referral</b>
									<ul>
										<li>The SEN team, teachers, special education teachers, coordinators and school administration establish uniform procedures for screening, referring, identifying and serving students from G3-9 who are gifted in general intellectual or specific academic aptitude.</li>
										<li>These screening processes are to be conducted annually.</li>
										<li>SEN team, teachers, coordinators and administration review data and scores to determine whether students are eligible for ALPs.</li>
										<li>SEN team will record at least three measures from the following categories:
											<ol>
												<li>Assessment of appropriate student products, performance, or portfolio</li>
												<li>Record of observation of in-class behavior</li>
												<li>Appropriate rating scales, checklists, or questionnaires</li>
												<li>Individual interviews</li>
												<li>Individually administered or group-administered aptitude or achievement test</li>
												<li>Record of previous accomplishments (such as awards, honors, grades etc.)</li>
												<li>Additional valid and reliable measures or procedures</li>
											</ol>
										</li>
										<li>SEN team will decide on an appropriate ALP to effectively meet the assessed students’ learning needs, which stresses on differentiated curriculum and instruction.</li>
										<li>SEN team will communicate closely with parents on a monthly/term basis.</li>
									</ul>
								</li>
							</ol>

							<h4>Policy for PLP</h4>

							<ol>
								<li><b>PLP Overview</b>
									<p>The purpose of this plan is to establish a process by which students with minor learning disabilities, who have not been deemed eligible for an IEP, but who require special education programs to achieve curriculum expectations and/or whose learning expectations are alternative to the expectations set out for a particular grade level.</p>
								</li>
								<li><b>Eligibility</b>
									<ul>
										<li>Students with ADD or ADHD</li>
										<li>Slow learners</li>
										<li>Students with dyslexia/ dyscalculia</li>
										<li>Lack of prior appropriate instruction in reading (phonemic awareness, phonics, fluency, vocabulary development, reading comprehension strategies)</li>
										<li>Lack of oral and written expression (limited English proficiency)</li>
										<li>Lack of prior appropriate instruction in math</li>
									</ul>
								</li>
								<li><b>Persons responsible for the development of the PLP</b>
									<ul>
										<li>At least one regular education teacher</li>
										<li>At least one special education teacher </li>
										<li>Subject coordinator</li>
										<li>SEN coordinator</li>
										<li>Parents</li>
										<li>Students (if appropriate for grades 6 and above)</li>
									</ul>
								</li>
								<li><b>Referral</b>
									<ul>
										<li>SEN team members receive notification of a proposed SEN meeting and SEN team meets and reviews referrals received.</li>
										<li>SEN team conducts a review of students’ academic and performance history, medical history (for ADD/ADHD) and conducts classroom observations in the area of concern.</li>
										<li>SEN team makes recommendations for interventions intended to improve the students’ academic performance and develop a PLP accordingly.</li>
										<li>SEN team monitors the students’ progress and reviews data weekly for discussion.</li>
										<li>SEN team communicates closely with parents every 90 days, and lets them know if goals/objectives have been met.</li>
										<li>SEN team reviews and updates the PLP goals every 90 days.</li>
										<li>SEN team terminates a child’s PLP, determining that the child is no longer eligible for a PLP and is capable of continuing in the mainstream classroom and achieve curriculum and grade level expectations.</li>
									</ul>
								</li>
							</ol>

							<h4>Policy for BMP</h4>

							<ol>
								<li><b>BMP overview</b>
									<p>The purpose of this plan is to clarify what behaviors are being targeted for change and how change will be handled. This plan is to be created once a series of interventions and strategies have been tried and have been unsuccessful.</p>
								</li>
								<li><b>Eligibility</b>
									<ul>
										<li>Students whose behavior in class affects their learning process</li>
										<li>Students with disabilities (to be included in their IEP or PLP if needed)</li>
										<li>Students involved in bullying</li>
										<li>Aggressiveness in the classroom and outside</li>
										<li>Students not complying to school and class rules</li>
									</ul>
								</li>
								<li><b>Persons responsible for the development of the BMP</b>
									<ul>
										<li>At least one regular education teacher</li>
										<li>At least one special education teacher</li>
										<li>Coordinator</li>
										<li>SEN Coordinator</li>
										<li>Supervisor</li>
										<li>School /external psychologist</li>
										<li>School counselor</li>
										<li>Parents</li>
										<li>Students (if appropriate for grades 6 and above)</li>
									</ul>
								</li>
								<li><b>Referral</b>
									<ul>
										<li>SEN team members receive notification of a proposed SEN meeting and SEN team meets and reviews referrals received.</li>
										<li>SEN team conducts a review of students’ academic and performance history, medical history and conducts classroom observations in the area of concern.</li>
										<li>SEN team makes recommendations for interventions intended to improve the students’ academic and behavioral performance.</li>
										<li>SEN team develops a BMP accordingly.</li>
										<li>SEN team monitors the students’ progress and reviews data weekly for discussion.</li>
										<li>SEN team communicates closely with parents every 90 days, and lets them know if goals/objectives of BMP have been met.</li>
										<li>SEN team reviews and updates the BMP goals every 90 days.</li>
									</ul>
								</li>
							</ol>

							<h4>Accommodations and Modifications for students with Special Needs</h4>

							<p>Any type of special education programs and related services specified in the IEP, PLP or ALP may be provided to students who qualify for services to meet their educational needs. In addition, in order to meet the educational needs of students, accommodations and modifications in the teaching methods, learning materials or learning environment may be necessary and should be specified in the plans. Accommodations are changes that do not alter what is being taught and includes alterations of the environment, curriculum format, or equipment that allows an individual with a disability to gain access to content and/or complete assigned tasks. Students who do not have special education plans but are eligible for special accommodations and modifications are to have a student profile compiled specifying in detail the special circumstances and accommodations.</p>

							<h4>Students currently on PLPs</h4>

							<ul>
								<li>SEN team will hold meeting with all persons involved regarding current students and PLPs, and revise their files and records/marks.</li>
								<li>Within 2/3 weeks, SEN team will agree on a decision and create appropriate PLPs where needed.</li>
								<li>In the case that a student is seen as capable of achieving curriculum and learning expectations, the PLP will be terminated but accommodations and modifications will be considered to facilitate the learning process.</li>
								<li>Teachers will check past marks and records and make plans for diagnostic assessments in order to have an early detection of students with disabilities and who are in need of learning support.</li>
							</ul>

							<h4>Weak and above average students</h4>

							<p>Differentiated instruction is imperative in any classroom to cater to students of all abilities and needs, incorporating different learning styles, language proficiency, background knowledge and readiness to learn etc. teachers need to target auditory, visual and kinesthetic learners in their approaches.</p>

							<h4>Addendum (A)- Pre-Referral</h4>

							<h4>Pre-referral – SEN Teams:</h4>

							<ul>
								<li>The school principal forms a SEN Team (SST) which consists of the school principal, SENCO, social worker, and specialist teacher(s). The student who is experiencing learning problems and a parent may also be required to participate in the SEN meetings.</li>
								<li>The SEN Team may meet to discuss the academic performance of students who are experiencing learning problems and have been referred to the SEN Team.</li>
							</ul>

							<h4>Addendum (B) – Referral</h4>

							<h4>Referral – SEN Teams:</h4>

							<ul>
								<li>If the student’s progress is adequate, interventions may be discontinued when no longer needed.</li>
								<li>If the student’s progress is not adequate, the SEN Team may refer the student to the zone Coordinator Team for the Multidisciplinary Evaluation Team (MET) to conduct a comprehensive evaluation and determine eligibility for the special education programs and related services.</li>
								<li>For students who are not enrolled in any school at the time that a request for an evaluation is made, or at the request of a parent, a comprehensive evaluation will be conducted to determine eligibility for special education services without completing Step 1 in the process.</li>
							</ul>

							<h4>Addendum (C) – Referral</h4>

							<h4>Dropping French or electives</h4>

							<ul>
								<li>If the student’s progress in English language skills is not adequate, the SEN team may request the student to drop French and/or elective classes to reinforce the language skills needed to achieve the learning goals.</li>
								<li>Dropping French/electives will be followed through only with parental consent.</li>
							</ul>

							<h4>Addendum (D) – Issues related to IEPs</h4>

							<h4>Mild learning disabilities</h4>

							<ul>
								<li>As of the academic year 2014-2015, DPS does not have the available resources to deal with moderate to severe cases or disabilities.</li>
								<li>To present date, DPS SEN Department handles mild cases stated in the PLP and BMP policies such as, dyslexia, ADHD, ADD, slow learner etc.</li>
								<li>We are continuously looking for speech therapists, psychologists and specialized teachers to meet a broader range of students’ learning needs.</li>
							</ul>

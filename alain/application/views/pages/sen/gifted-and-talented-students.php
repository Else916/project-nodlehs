							<h3 class="title"><span>Gifted and Talented Students</span></h3>

							<p><b>Spatial Thinking</b> - is thinking about space and our bodies in space. It is about objects to each other in space, and transforming objects in space - rotating, flipping, scaling, composing or decomposing them. It is also about the space on a paper and our understanding of spatial symbolic systems including maps, graphs and diagrams.</p>

							<p><b>Verbal Thinking</b> - it is understanding and reasoning using concepts framed in words. It aims at evaluating ability to think constructively, rather than at simple fluency or vocabulary recognition.</p>

							<p><b>Quantitative Reasoning</b> - Students use numbers or symbols representing measurement, properties and the relationship of quantities to reason formally within abstract systems of thought to make decisions, judgments or predictions.</p>

							<p><b>Non-verbal Reasoning</b> - Nonverbal is all reasoning that not verbal, which involves drawing conclusions based on a set of facts either fictional or non-fictional.</p>


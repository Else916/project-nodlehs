							<h4><center>DPS Anti-Bullying Policy</center></h4>

							
							<p><b>This document is a statement of the aims, principals and strategies for the prevention of bullying at Al Dhafra Private School. All our students have the right to work in an environment which is safe from intimidation and in which all types of bullying are regarded unacceptable.</b></p>

							<h4><b><u>Definition:</u></b></h4>
							
							<p>Bullying is aggressive behavior that is intentional and that involves an imbalance of power or strength. The definition includes a wide range of behavior, whether verbal, written, physical or social, targeting a person or property.</p>
							
							
							

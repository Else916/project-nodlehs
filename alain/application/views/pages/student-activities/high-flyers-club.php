<h3 class="title"><span>High Flyers Club - Primary English Department</span></h3>
<br>
	<h4 class="sub-title">Club for Gifted/High Achievers in English</h4>

	<p>Objective:</p>

	<ul>
		<li>To offer gifted and talented students extra stimulation.</li>
		<li>To enhance the students’ critical thinking and analysis skills.</li>
		<li>To offer practice in public speaking.</li>
		<li>To develop students’ communication and social skills.</li>
	</ul>

	<p>Members: Grades 1- 6<br>This club is only offered to students who have been designated as gifted and talented by their teachers.</p>

	<p>Meeting Times:</p>

	<ul>
		<li>2:30–3:30pm every Sunday – Grades 1-3 + 4-6 (Girls)</li>
		<li>2:30–3:30pm every Tuesday – Grades 4-6 (Boys)</li>
	</ul>

	<p>The club stops meeting a week prior to the start of school examinations.</p>


		<li>To promote environmental awareness and develop social and life skills.</li>
		<li>To increase students awareness of environmental issues and give them power to make change.</li>
		<li>To provide opportunities for students to become directly involved in a variety of projects relating to the environment.</li>
		<li>To empower students to take action and work toward building a sound environmental ethic.</li>
	</ul>

	<h4 class="sub-title">Environmental Education Mission:</h4>


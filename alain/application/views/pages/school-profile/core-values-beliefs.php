	<h3 class="title"><span>Core Values and Beliefs</span></h3>
							<br>
  						    <p class="qStyle">At Al Dhafra Private Schools we believe in:</p>
							<br>
							<p>
								1. Transparency, Fairness, and Justice – everyone will know what is expected of them, and will understand how performance is judged. We will be clear, sincere, and honest.<br>
								2. Respect and Equality – we will treat everyone with respect, value and embrace diversity, and show our appreciation for effort and achievement.<br>
                                3. Cooperation, Collaboration, and Teamwork – we will work well together and will support each other in achieving the targets and goals of the school.<br>
                                4. Care and Compassion – we will be tolerant, and will treat people as we would like to be treated ourselves.<br>
                                5. Honesty and Integrity – we will always act with integrity and honesty, regardless of the circumstances, but will do so in a sensitive and thoughtful way.<br>
                                6. Accountability – We will accept a culture of professional and personal accountability, in which we strive for the highest standards.<br>
 							</p>

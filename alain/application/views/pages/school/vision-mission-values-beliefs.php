							<h3 class="title"><span>School's Vision, Mission, Values and Beliefs</span></h3>

							<p>The following Vision, Mission, Values and Beliefs summarize our stakeholders’ expectations and feedback for an effective school (from 6/2011–1/ 2012):</p>
							<p><b>Vision</b> – To be an innovative, comprehensive school whereby students are engaged daily in the highest levels of learning across the curriculum.</p>
							<p><b>Mission</b> – To achieve our primary objectives outlined in the *School Improvement Plan and school Vision by 2020.</p>
							<p>Develop an effective school in five key areas:</p>
							<ol>
								<li>Leadership</li>
								<li>Teaching and Learning</li>
								<li>Student Progress and Attainment</li>
								<li>Student Welfare</li>
								<li>Stakeholder Expectations</li>
							</ol>
							<p>Establish high quality teaching across the curriculum.</p>
							<p>Foster a positive, student-centered learning environment amongst all stakeholders across the curriculum.</p>
							<p>Raise the level of teaching, learning and progress amongst staff and students across the curriculum.</p>
							<p>Ensure the curriculum meets the comprehensive needs of all students.</p>
							<p><b>Values and Beliefs</b> – We value and believe in each student’s:</p>
							<ul>
								<li><b>Right to Learn</b> – All students are provided opportunities to learn in and out of the class across the curriculum.</li>
								<li><b>Progress and Attainment</b> – All students, albeit unique in their standards and learning styles, are challenged to progress and attain the highest levels of academic standards across the curriculum.</li>
								<li><b>Personal Development</b> – All students are guided to become morally, socially and environmentally responsible, global citizens, as well as independent free-thinkers across the curriculum.</li>
								<li><b>Safety</b> – All students have the right to an intellectually, emotionally, morally, and physically safe and secure learning environment across the curriculum.</li>
							</ul>

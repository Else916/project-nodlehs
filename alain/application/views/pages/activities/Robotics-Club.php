	<h3 class="title"><span>Robotics Club</span></h3>

		<p>Robotics is a wonderful platform for learning 21st century skills. Solving robotic challenges builds innovation, creativity and problem solving skills in students, and because robotics cross multiple curriculum subjects, students must learn and apply their knowledge in science, engineering, math, and computer programming. The most rewarding part of designing robots is that students have fun, work together as a team, and learning occurs as naturally as breathing air.</p>
		<p></p>

	<h4><i>Objectives</i></h4>
		<ol>
		  <li>Provide school students with an opportunity to expand their horizons through exploration of robots and robotic systems in the school.</li>
		  <li>Introduce the concept of modern science into schools' educational activities in science and technology.</li>
		  <li>Provide an opportunity to promote creative thinking, improve communication and cooperation skills and strengthen the ability to acquire new knowledge highly relevant towards progressive education.</li>
		  <li>Bring together young people from all over the world to develop creativity and problem solving skills through challenging and educational robot competitions.</li>
		</ol>

							<p>English is the medium of instruction for all courses except for Islamic Studies, Arabic Language and Arabic Social Studies which are taught in Arabic as stipulated by the U.A.E. Ministry of Education.</p>

							<p>The curriculum at kindergarten, primary, intermediate and secondary levels prepares the foundation for the students to sit for the IGCSE examinations administered by the University of Cambridge (UK) taken at the end of grade 10/11. Students in grade 12 are able to continue for their AS and A-Levels as well as for their High School Certificate where they can sit for SAT 1 in grade 11, TOEFL and SAT 2 in grade 12.</p>

							<p>Islamic studies are obligatory for all Muslim Arab and Non-Arab students.</p>

							<p>Arabic is obligatory for all students.</p>

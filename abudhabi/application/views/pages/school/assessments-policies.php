<h3>Essential Agreement on Assessing</h3>
<h4>Continuous Assessment (CAS):</h4>
<p>Key features of ongoing, continuous assessment – there are two principle components of the ongoing assessment process:<p>
  <ol>
    <li>Establishing criteria</li>
    <li>Providing feedback</li>
  </ol>
<p>1.	Criteria for each performance of understanding need to be:</p>
<ul>
  <li>Clear – articulated explicitly at the beginning of each performance of understanding – though may well evolve the course of the performance, especially if it is new to the teacher as well as the students.</li>
  <li>Relevant – closely related to the understanding goals for the standard(s) or unit.</li>
  <li>Public – everyone in the classroom knows and understands them.</li>
</ul>
<p>2.	Feedback needs to:</p>
<ul>
  <li>Occur frequently – from the beginning of the unit to its conclusion, in conjunction with performances of understanding. On some occasions feedback may be formal and planned (such as those related to presentations); some maybe more casual and informal (such as responding to a student’s comment in a class discussion).</li>
  <li>Be informative – provide students with not only information about how well they have carried out performances, but also how they might improve them.</li>
  <li>Guide the teaching and learning process – inform your planning of subsequent classes and activities.</li>
  <li>Come form a variety of perspectives – from the teacher(s), students’ reflection on their own work and from classmates reflecting on one another’s work.</li>
</ul>

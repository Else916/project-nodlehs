
	<h3>School's Code of Conduct</h3>

	<p>Our school is a place that promotes and fosters responsibility, respect, as well as academic and social excellence in a safe teaching and learning environment.</p>
	<p>All stakeholders (students, parents, teachers, staff and community members) have the right to be and feel safe in our school community. With this right comes the responsibility to be conscientious, honorable citizens and to be accountable for one’s actions.</p>
	<p>Standards of Behavior: The ABCs for operational conduct, therefore, all school members are expected and encouraged to:</p>

	<p><b style="font-weight:bold;">Ask questions</b> – a key component to learning and teamwork comes from asking questions. If ever in doubt or confused about any particular task or procedure, ask the direct person responsible or to be guided to the right person or policy.</p>

	<p><b style="font-weight:bold;">Be punctual</b> – on time to school, class, assemblies, in submitting class and homework assignments, and carrying out assigned duties, etc.</p>

	<p><b style="font-weight:bold;">Communicate professionally</b> – at the right time, in the right place, in the right tone, with the right person(s) in a respectful, sensible manner according to the policies.</p>

	<p><b style="font-weight:bold;">Demonstrate honesty, integrity, respect and fairness</b> – with our self, others, as well as others’ and the school’s properties and belongings.</p>

	<p><b style="font-weight:bold;">Email and ICT</b> – as a 21st century school and country, communication and best practice via ICT is promoted, professional, efficient and effective.</p>

	<p><b style="font-weight:bold;">Follow rules and expectations</b> – the school’s and local authorities’.</p>

	<p><b style="font-weight:bold;">Get involved</b> – with the students’ learning, school’s vision, activities, and with the stakeholders.</p>

	<p><b style="font-weight:bold;">Have the right to communicate and challenge</b> – in an appropriate, respectful, sensible and timely manner, if he or she believes or feels his or her rights have been violated.</p>

	<p><b style="font-weight:bold;">In no way, participate in verbal or physical altercations</b> – all concerns should be communicated immediately to the appropriate personnel. For more on staff code of conduct, please refer to ADEC published documents.</p>


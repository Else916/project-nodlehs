<h3 class="title"><span>Graduation Requirements</span></h3>
<br>
							<p>Equivalence of Secondary School Completion Certificates of Private schools applying Foreign Curricula:</p>

							<p>It has been observed, through the inquiry of male and female students who studied curricula other than the ministry ones and who are willing to equal their school certificates, that some of them did not adhere to the conditions stated in the Ministerial Decision No. (4443) of 2001 regarding the Regulatory Manual of certificate equivalence, some of which are related to grade 10 & 11 , making it difficult to carry out the process of equivalence, a fact that embarrassed students and their parents because of the absence of any solutions thereof.</p>

							<p>Therefore, we would like to illustrate the required conditions to accept the equivalence so as adhere to them and to explain the same to students.</p>

							<p>Those who are unwilling to adhere and observe the a/m conditions are expected to sign a declaration wherein they express their unwilling of obtaining equivalence and to send a copy of the said declaration to Private Education Department at the Zone.</p>

							<h4 class="sub-title">FIRST: THE BRITISH CURRICLUM</h4>

							<p>Student who studied the O.L subjects of Grade 10 are requested to do the following:</p>

							<ul>
								<li>Five (O.L.) subjects minimum of grade 10.</li>
								<li>Two (A.S) subjects minimum of grade 11 which merit shall not be led than (D).</li>
								<li>Two (A.S) subjects or one (A.L) subject minimum of grade 12, which merit shall not be less than (D).</li>
								<li>The studying of Arabic in grades 10, 11, 12 for all students (for native and non-native speakers of Arabic) and the study of Islamic Education for all Muslim students.</li>
								<li>Setting for common Exam at the Zone level in Arabic & Islamic Education of Grade 12.</li>
								<li>The student should get 60% in Arabic and Islamic Education in grades 10, 11 and 12.</li>
								<li>Study for 12 successive academic years.</li>
							</ul>

							<p>Notes on equivalence of certificates of students who study the British Syllabus:</p>

							<ul>
								<li>Each school year should have the required minimum of subjects.</li>
								<li>Subject can never be transferred from a school year to another (e.g. to study 3 subjects in grade 11 A.S and one in grade 12 and then to demand transferring a third subject from grade 11 to grade 12).</li>
							</ul>

							<p>On the other hand, the total counting of subjects without being distributed over the three years is not possible.</p>

							<ul>
								<li>Arabic and Islamic Education are not counted as A.L, A.S or O.L subjects.</li>
								<li>Only Subjects completed in June are accepted but not those completed in November expect for those taken to complete the school year subjects provided that they should not be the same subjects completed in June or used to change the result. If a student completed a specific subject in November 2010, it followed what he has accomplished in June 2010 Academic Year 2009/2010 and so on, and, consequently, cannot be annexed subject completed in June 2011. With the knowledge that completing any subjects in November will necessarily lead to delay the issuance of the certificate equivalence until the results of November appear.</li>
								<li>To accept the equivalence, the student should complete 12 school years, and it cannot be considered as a complete school year if the student studied only Arabic and Islamic Education.</li>
								<li>The applicant student in grade 12 shall set for Arabic and Islamic Education exams written and administered by the Education zone scoring not less 60%.</li>
							</ul>


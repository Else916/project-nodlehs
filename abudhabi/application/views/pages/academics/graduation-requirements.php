							<h4 class="sub-title">SECOND: THE AMERICAN CURRICULUM</h4>

							<ul>
								<li>To obtain the certificate of US High School after studying 12 years, and it equals one of the following certificates:
									<ul>
										<li>High school Diploma</li>
										<li>High School Certificate</li>
										<li>Graduation Certificate</li>
									</ul>
								</li>
								<li>School certificates grades 10, 11, and 12 should include at least five subjects in addition to Arabic for native and non native speakers, and Islamic education for Muslims provided that the average of the student is 60% or above in all subjects. Any subject less than 60% shall not be counted as one of the required subjects.</li>
								<li>TOFEL with a minimum of 61.</li>
								<li>(Sat 1) with a minimum of 400 in mathematics.</li>
							</ul>

							<p>Notes related about Certification equalization of students who are studying the American Curriculum:</p>

							<ul>
								<li>Studying of Arabic and Islamic education are implemented according to the curriculum and study plan in public schools and examinations in these two subjects are standardized (unified) at the level of the educational zone and under its supervision in the twelfth grade.</li>
								<li>The degree of success in all subjects in grade 10, 11 and 12 is 60% and applies to both Arabic and Islamic education.</li>
								<li>Only the original International TOFEL transcript provided that its holder shall attain a score not less than 61, nor will the original SAT1 certificate will be approved if its score is less than 400 in Math.</li>
								<li>Students must be registered as students studying for the American approach, according to a copy enrollment in private education section in the zone or the ministry.</li>
							</ul>
